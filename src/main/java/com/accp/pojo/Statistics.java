package com.accp.pojo;

public class Statistics {
	
	private int statisticsId;
	private int year;
	private int month;
	private float withdrawCash;
	private float rechargeCash;
	private float serviceMoney;
	private float advertisementMoney;
	private int  businessCount;
	public int getStatisticsId() {
		return statisticsId;
	}
	public void setStatisticsId(int statisticsId) {
		this.statisticsId = statisticsId;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public float getWithdrawCash() {
		return withdrawCash;
	}
	public void setWithdrawCash(float withdrawCash) {
		this.withdrawCash = withdrawCash;
	}
	public float getRechargeCash() {
		return rechargeCash;
	}
	public void setRechargeCash(float rechargeCash) {
		this.rechargeCash = rechargeCash;
	}
	public float getServiceMoney() {
		return serviceMoney;
	}
	public void setServiceMoney(float serviceMoney) {
		this.serviceMoney = serviceMoney;
	}
	public float getAdvertisementMoney() {
		return advertisementMoney;
	}
	public void setAdvertisementMoney(float advertisementMoney) {
		this.advertisementMoney = advertisementMoney;
	}
	public int getBusinessCount() {
		return businessCount;
	}
	public void setBusinessCount(int businessCount) {
		this.businessCount = businessCount;
	}
	
	 
	 
}
